package com.stellaris.model;

public class DorRoom {
    private String id;
    private String shortId;
    private String buiId;
    private String schId;
    private String stus;
    private String stuCount;
    private String powerRate;
    private String waterRate;
    private String lastReqDate;
    private String events;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getShortId() {
        return shortId;
    }

    public void setShortId(String shortId) {
        this.shortId = shortId;
    }

    public String getBuiId() {
        return buiId;
    }

    public void setBuiId(String buiId) {
        this.buiId = buiId;
    }

    public String getSchId() {
        return schId;
    }

    public void setSchId(String schId) {
        this.schId = schId;
    }

    public String getStus() {
        return stus;
    }

    public void setStus(String stus) {
        this.stus = stus;
    }

    public String getStuCount() {
        return stuCount;
    }

    public void setStuCount(String stuCount) {
        this.stuCount = stuCount;
    }

    public String getPowerRate() {
        return powerRate;
    }

    public void setPowerRate(String powerRate) {
        this.powerRate = powerRate;
    }

    public String getWaterRate() {
        return waterRate;
    }

    public void setWaterRate(String waterRate) {
        this.waterRate = waterRate;
    }

    public String getLastReqDate() {
        return lastReqDate;
    }

    public void setLastReqDate(String lastReqDate) {
        this.lastReqDate = lastReqDate;
    }

    public String getEvents() {
        return events;
    }

    public void setEvents(String events) {
        this.events = events;
    }


}
